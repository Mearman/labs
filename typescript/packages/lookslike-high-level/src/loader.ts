export function truncateAsciiArt(n: number) {
  if (n < 1) return "";

  const lines = asciiArt.trim().split("\n");
  let result = "";
  let charCount = 0;

  for (let i = 0; i < lines.length && charCount < n; i++) {
    for (let j = 0; j < lines[i].length && charCount < n; j++) {
      result += lines[i][j];
      charCount++;
    }
    if (charCount < n) {
      result += "\n";
      charCount++;
    }
  }

  if (Number.isInteger(n) === false) {
    result += ".";
  }

  return result;
}
